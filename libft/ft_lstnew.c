#include <stdlib.h>
#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size, char letter)
{
	t_list *new;

	if (!(new = (t_list*)malloc(sizeof(*new))))
		return (NULL);
	if (!content)
	{
		new->content = NULL;
		new->content_size = 0;
		new->letter = 0;
	}
	else
	{
		if (!(new->content = malloc(content_size)))
			return (NULL);
		ft_memcpy(new->content, content, content_size);
		new->content_size = content_size;
		new->letter = letter;
	}
	new->tab = NULL;
	new->next = NULL;
	return (new);
}
